'use strict'

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const m = require('mithril')
const mnr = require('mithril-node-render')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
const render = (data, els) =>
  [ '<!DOCTYPE html>'
  , mnr(m('html', {lang: 'en-us'}, els.map(el => el(data))))
  ].join('')

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = render
