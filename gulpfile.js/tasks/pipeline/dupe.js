'use strict'

/*
 * duplicate unchanged source files to dist
 */

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const gulp = require('gulp')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
function dupe() {
  return gulp.src('source/static')
    .pipe(gulp.dest('dist'))
}

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = dupe
gulp.task(dupe)
