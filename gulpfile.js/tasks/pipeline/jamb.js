'use strict'

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const Jamb = require('jamb')
const gulp = require('gulp')

// local
const cfg = require('conf/jamb')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
function jamb(cb) {
  new Jamb(cfg).then(() => cb())
}

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = jamb
gulp.task('jamb', jamb)
