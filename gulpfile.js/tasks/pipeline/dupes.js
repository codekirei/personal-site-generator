'use strict'

/*
 * duplicate unchanged source files to dist
 */

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const gulp = require('gulp')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
function dupes() {
  return gulp.src('source/static')
    .pipe(gulp.dest('dist'))
}

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = dupes
gulp.task(dupes)
