'use strict'

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const gulp = require('gulp')
const browserSync = require('browser-sync')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
function serve() {
  browserSync({
    server: {
      baseDir: 'dist'
    }
  })
}

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = serve
gulp.task(serve)
