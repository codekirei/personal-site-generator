'use strict'

/*
 * completely deletes dist dir
 */

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const gulp = require('gulp')
const del = require('del')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
function clean(cb) {
  return del(['dist/**/*', '!.git']).then(() => cb())
}

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = clean
gulp.task(clean)
